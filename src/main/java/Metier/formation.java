package Metier;

public class formation {

	private int id_formation;
	private String nom;
	private String domaine;
	private String  niveux_formation;
	private String niveux_etude;
	
	
	
	
	
	public int getId_formation() {
		return id_formation;
	}
	public void setId_formation(int id_formation) {
		this.id_formation = id_formation;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getDomaine() {
		return domaine;
	}
	public void setDomaine(String domaine) {
		this.domaine = domaine;
	}
	public String getNiveux_formation() {
		return niveux_formation;
	}
	public void setNiveux_formation(String niveux_formation) {
		this.niveux_formation = niveux_formation;
	}
	public String getNiveux_etude() {
		return niveux_etude;
	}
	public void setNiveux_etude(String niveux_etude) {
		this.niveux_etude = niveux_etude;
	}
	
	
	
	
	
}
